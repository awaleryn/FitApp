package com.example.fitapp.calories;

public enum ActivityLevel {
    SEDENTARY,         // Little or no physical activity
    LIGHTLY_ACTIVE,    // Light physical activity (e.g. light exercise/sport 1-3 days a week)
    MODERATELY_ACTIVE, // Moderate physical activity (e.g. moderate exercise/sport 3-5 days a week)
    VERY_ACTIVE,       // High physical activity (e.g. intense exercise/sport 6-7 days a week)
    EXTRA_ACTIVE       // Very intense physical activity (e.g. very intense exercise/sport and physical work)
}
